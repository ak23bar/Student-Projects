num1= float(input("choose any number:"))
num2= float(input("Choose any second number:"))
op= input("choose an operation given +:addition, -:subtraction, *:multiplication, /:divison:")
if op =='+':
    print("calculating ...")
    print(num1+num2)
if op =='-':
    print("calculating ...")
    print(num1-num2)
if op =='*':
    print("calculating ...")
    print(num1*num2)
if op =='/':
    print("calculating ...")
    print(num1/num2)